#include <iomanip>

#include "Item.h"

//------------------------------------------------------------------------------
Item::Item()
    :Item(0, "Air")
{
}

//------------------------------------------------------------------------------
Item::Item(int i)
    :Item(i, "Air")
{
}

//------------------------------------------------------------------------------
Item::Item(int i, std::string n)
    :name(n)
{
    this->id = i;
}

//------------------------------------------------------------------------------
void Item::display(std::ostream &outs) const
{
    outs << std::right << std::setw(3) << id << " " << name;
}
