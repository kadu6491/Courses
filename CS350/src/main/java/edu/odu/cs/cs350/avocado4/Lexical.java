package edu.odu.cs.cs350.avocado4;

/**
 * Lexical Enum
 * 
 * Enumeration for the Lexical attribute in FeatureSet.
 * The different types of PartOfSpeech are listed in this class.
 * Cataloger sets these in respective methods.
 * 
 */
public enum Lexical {
    NUMBER,
    PUNCTUATION,
    CAPLETTER,
    CAPITAL,
    ALLCAPS,
    NEW_LINE,
    OTHER,
    NULL
}
